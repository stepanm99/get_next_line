#include "libft.h"

void	ft_bzero(void *s, size_t l)
{
	size_t	i;
	char	*ptr;

	ptr = s;
	i = 0;
	while (l > 0)
	{
		ptr[i] = '\0';
		i++;
		l--;
	}
}
